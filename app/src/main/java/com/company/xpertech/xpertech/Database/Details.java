public class Details {
    String accountNumber;
    String box_number;
    String troubleshoot_id;

    public Details() {
    }

    public String getAccountNumber() {
        return accountNumber;
    }

    public void setAccountNumber(String accountNumber) {
        this.accountNumber = accountNumber;
    }

    public String getBox_number() {
        return box_number;
    }

    public void setBox_number(String box_number) {
        this.box_number = box_number;
    }

    public String getTroubleshoot_id() {
        return troubleshoot_id;
    }

    public void setTroubleshoot_id(String troubleshoot_id) {
        this.troubleshoot_id = troubleshoot_id;
    }
}
