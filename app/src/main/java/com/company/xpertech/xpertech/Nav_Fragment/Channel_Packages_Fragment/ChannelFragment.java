    /**
     *  Task to query for the list of channel in regards to the package selected to be displayed.
     *  Task to query for the list of channel in regards to the package selected to be displayed.
     */
    public class ChannelTask extends AsyncTask<String,Void,String> {
        Context ctx;
        AlertDialog alertDialog;

        public ChannelTask(Context ctx)
        {
            this.ctx =ctx;
        }

        @Override
        protected void onPreExecute() {
            alertDialog = new AlertDialog.Builder(ctx).create();
            alertDialog.setTitle("");
        }
        @Override
        protected String doInBackground(String... params) {
            String packages_url = "https://uslsxpertech.000webhostapp.com/xpertech/channels.php";
            String method = params[0];
            if(method.equals("channel")){
                String package_id = params[1];
                try {
                    URL url = new URL(packages_url);
                    HttpURLConnection httpURLConnection = (HttpURLConnection)url.openConnection();
                    httpURLConnection.setRequestMethod("POST");
                    httpURLConnection.setDoOutput(true);
                    httpURLConnection.setDoInput(true);
                    OutputStream outputStream = httpURLConnection.getOutputStream();
                    BufferedWriter bufferedWriter = new BufferedWriter(new OutputStreamWriter(outputStream,"UTF-8"));
                    String data = URLEncoder.encode("package_id","UTF-8")+"="+URLEncoder.encode(package_id,"UTF-8");
                    bufferedWriter.write(data);
                    bufferedWriter.flush();
                    bufferedWriter.close();
                    outputStream.close();
                    InputStream inputStream = httpURLConnection.getInputStream();
                    BufferedReader bufferedReader = new BufferedReader(new InputStreamReader(inputStream,"UTF-8"));
                    String response = "";
                    String line = "";
                    line = bufferedReader.readLine();
                    String[] name = line.split("\\$");
                    for(int i = 0; i < name.length; i++)
                    {
                        channelsList.add(new Channels(name[i]));
                    }
                    bufferedReader.close();
                    inputStream.close();
                    httpURLConnection.disconnect();
                    return response;
                } catch (MalformedURLException e) {
                    e.printStackTrace();
                } catch (IOException e) {
                    e.printStackTrace();
                }
            }
            return null;
        }
        @Override
        protected void onProgressUpdate(Void... values) {
            super.onProgressUpdate(values);
        }

        @Override
        protected void onPostExecute(String result) {
            display();
        }
    }
